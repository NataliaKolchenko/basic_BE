public class Compare {

    public static void main(String[] args) {

        int a = 1;
        int b = 2;
        int c = 3;

        System.out.println(a>b);
        System.out.println(a<=b);
        System.out.println(a>c);
        System.out.println(a<=c);
        System.out.println(b<c);
        System.out.println(b>=c);
        System.out.println(a==b);
        System.out.println(a==c);
        System.out.println(b==c);
        System.out.println(a!=b);
        System.out.println(a!=c);
        System.out.println(b!=c);


    }
}


